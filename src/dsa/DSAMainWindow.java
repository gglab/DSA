    private void generateDSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateDSActionPerformed
        dsa = new DSA();
        signature.put("p", dsa.getP());
        signature.put("q", dsa.getQ());
        signature.put("g", dsa.getG());
        signature.put("y", dsa.getPublicKeyY());
        signature.putAll(dsa.sign(input.getText().getBytes()));
        r.setText(signature.get("r").toString(16));
        s.setText(signature.get("s").toString(16));
        if (showParamsGen.isSelected()) {
            q.setText(signature.get("q").toString(16));
            p.setText(signature.get("p").toString(16));
            g.setText(signature.get("g").toString(16));
            y.setText(signature.get("y").toString(16));
        }
    }//GEN-LAST:event_generateDSActionPerformed

    private void verifyDSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verifyDSActionPerformed
        signature.put("r", new BigInteger(newR.getText(), 16));
        signature.put("s", new BigInteger(newS.getText(), 16));
        if (showParamsImp.isSelected()) {
            signature.put("p", new BigInteger(newP.getText(), 16));
            signature.put("q", new BigInteger(newQ.getText(), 16));
            signature.put("g", new BigInteger(newG.getText(), 16));
            signature.put("y", new BigInteger(newY.getText(), 16));
        }
        final boolean isValid = DSA.verify(toVerification.getText().getBytes(), signature);
        if (isValid) {
            showInfo("Weryfikacja podpisu cyfrowego dla danych powiodła się", "Weryfikacja OK!");
        } else {
            showError(new Exception(), "Weryfikacja zakończona niepowidzeniem!", "Błąd weryfikacji");
        }
    }//GEN-LAST:event_verifyDSActionPerformed

