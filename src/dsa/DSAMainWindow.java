    void saveDS(final File file) {
        try {
            FileOutputStream fileOut = new FileOutputStream(file);
            ObjectOutputStream out = new ObjectOutputStream(fileOut);
            out.writeObject(signature);
            out.close();
            fileOut.close();
        } catch (IOException i) {
            i.printStackTrace();
        }
    }

    void setDSFromFile(final File file) {
        try {
            FileInputStream fileIn = new FileInputStream(file);
            ObjectInputStream in = new ObjectInputStream(fileIn);
            signature = (HashMap<String, BigInteger>) in.readObject();
            in.close();
            fileIn.close();
            newR.setText(signature.get("r").toString(16));
            newS.setText(signature.get("s").toString(16));
            if (showParamsImp.isSelected()) {
                newY.setText(signature.get("y").toString(16));
                newQ.setText(signature.get("q").toString(16));
                newP.setText(signature.get("p").toString(16));
                newG.setText(signature.get("g").toString(16));
            }
        } catch (IOException i) {
            i.printStackTrace();
        } catch (ClassNotFoundException c) {
            c.printStackTrace();
        }
    }
